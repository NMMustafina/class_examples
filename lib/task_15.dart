      print('–ü–ª–æ—â–∞–¥—å —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–∞: ${sqrt(p * (p - a) * (p - b) * (p - c))}');
    }
  }

  @override
  void perimeter() {
    if (a == b && a == c) {
      print("–ü–µ—Ä–∏–º–µ—Ç—Ä —Ä–∞–≤–Ω–æ—Å—Ç–æ—Ä–æ–Ω–Ω–µ–≥–æ —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–∞: ${3 * a}");
    } else {
      print("–ü–µ—Ä–∏–º–µ—Ç—Ä —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–∞: ${a + b + c}");
    }
  }
}
