abstract class Shape {
  void area();
  void perimeter();
}

class Circle extends Shape {
  double radius;

  Circle({required this.radius});

  @override
  void area() => print('–ü–ª–æ—â–∞–¥—å –∫—Ä—É–≥–∞: ${3.1416 * radius * radius}');

  @override
  void perimeter() => print("–î–ª–∏–Ω–∞ –æ–∫—Ä—É–∂–Ω–æ—Å—Ç–∏: ${2 * 3.1416 * radius}");
}

class Rectangle extends Shape {
  double width;
  double height;

  Rectangle({required this.width, required this.height});

  @override
  void area() => print('–ü–ª–æ—â–∞–¥—å –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–∞: ${width * height}');

  @override
