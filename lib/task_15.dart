}

class Triangle extends Shape {
  double a;
  double b;
  double c;

  Triangle({required this.a, required this.b, required this.c});

  @override
  void area() {
    if (a == b && a == c) {
      print('–ü–ª–æ—â–∞–¥—å —Ä–∞–≤–Ω–æ—Å—Ç–æ—Ä–æ–Ω–Ω–µ–≥–æ —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–∞: ${(sqrt(3) / 4) * a * a}');
